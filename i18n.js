window.siteTranslations = siteTranslations;

function getSiteLang() {
  return localStorage.getItem("site-lang") || "en";
}

function setSiteLang(lang) {
  localStorage.setItem("site-lang", lang);
}

function applySiteTranslations(lang) {
  const dict = siteTranslations[lang] || siteTranslations.en;
  const pageKey = document.body?.dataset.page || "home";
  const seo = dict.seo?.[pageKey];

  document.documentElement.lang = lang === "uk" ? "uk" : "en";

  if (seo?.title) {
    document.title = seo.title;
  }

  const metaDescription = document.querySelector('meta[name="description"]');
  if (metaDescription && seo?.description) {
    metaDescription.setAttribute("content", seo.description);
  }

  const metaKeywords = document.querySelector('meta[name="keywords"]');
  if (metaKeywords && seo?.keywords) {
    metaKeywords.setAttribute("content", seo.keywords);
  }

  const ogTitle = document.querySelector('meta[property="og:title"]');
  if (ogTitle && seo?.ogTitle) {
    ogTitle.setAttribute("content", seo.ogTitle);
  }

  const ogDescription = document.querySelector('meta[property="og:description"]');
  if (ogDescription && seo?.ogDescription) {
    ogDescription.setAttribute("content", seo.ogDescription);
  }

  const twitterTitle = document.querySelector('meta[name="twitter:title"]');
  if (twitterTitle && seo?.twitterTitle) {
    twitterTitle.setAttribute("content", seo.twitterTitle);
  }

  const twitterDescription = document.querySelector('meta[name="twitter:description"]');
  if (twitterDescription && seo?.twitterDescription) {
    twitterDescription.setAttribute("content", seo.twitterDescription);
  }

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n");
    if (dict[key]) {
      element.textContent = dict[key];
    }
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const key = element.getAttribute("data-i18n-placeholder");
    if (dict[key]) {
      element.setAttribute("placeholder", dict[key]);
    }
  });

  const toggle = document.getElementById("lang-toggle");
  if (toggle) {
    toggle.classList.remove("ua", "en");
    if (dict.toggleClass) {
      toggle.classList.add(dict.toggleClass);
    }
    toggle.innerHTML = `<span class="flag">${dict.toggleFlag}</span><span class="lang-code">${dict.toggleLabel}</span>`;
  }
}

window.applySiteTranslations = applySiteTranslations;

document.addEventListener("DOMContentLoaded", () => {
  let currentLang = getSiteLang();
  applySiteTranslations(currentLang);

  const toggle = document.getElementById("lang-toggle");
  if (toggle) {
    toggle.addEventListener("click", () => {
      currentLang = currentLang === "en" ? "uk" : "en";
      setSiteLang(currentLang);
      applySiteTranslations(currentLang);
      document.dispatchEvent(new CustomEvent("site-language-change", { detail: { lang: currentLang } }));
    });
  }
});
